// a small C++ program
#include <iostream>

int main()
{	//0.0 ex (the original hello world program)
	std::cout << "Hello, world!" << std::endl;

	//0.2 ex (print that sentence)
	std::cout << "This (\") is a quote, and this (\\) is a backslash." << std::endl;
	
	//0.4 ex (print the original program)
	std::cout << "int main()\n";
	std::cout << "{\n";
	std::cout << "\t std::cout << \"Hello, world!\" << std::endl;\n";
	std::cout << "\t return 0;\n";
	std::cout << "}\n";
	return 0;
}